CREATE TABLE Log4Net
( 
	"Id" NUMBER NOT NULL ENABLE,
	"Date" DATE NOT NULL,
	"Thread" VARCHAR2 (16) NOT NULL,
	"AppID" VARCHAR2(20) NULL,
	"Level" VARCHAR2(16) NOT NULL,
	"Logger" VARCHAR2(255) NOT NULL,
	"Message" VARCHAR2(4000) NOT NULL,
	"Exception" VARCHAR2(4000) NULL,
 CONSTRAINT "LOG4NET_PK" PRIMARY KEY ( "Id" ) ENABLE
 );
 
 CREATE SEQUENCE "LOG4NET_SEQ" MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 NOCACHE NOORDER NOCYCLE;
 